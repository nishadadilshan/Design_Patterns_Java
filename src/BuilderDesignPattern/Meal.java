package BuilderDesignPattern;

public class Meal {
    private String curry;
    private String bread;
    private String coolDrink;
    private String biriyani;

    public String getCurry() {
        return curry;
    }

    public void setCurry(String curry) {
        this.curry = curry;
    }

    public String getBread() {
        return bread;
    }

    public void setBread(String bread) {
        this.bread = bread;
    }

    public String getCoolDrink() {
        return coolDrink;
    }

    public void setCoolDrink(String coolDrink) {
        this.coolDrink = coolDrink;
    }

    public String getBiriyani() {
        return biriyani;
    }

    public void setBiriyani(String biriyani) {
        this.biriyani = biriyani;
    }
}
